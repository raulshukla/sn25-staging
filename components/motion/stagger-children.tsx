import { motion, HTMLMotionProps } from "framer-motion";

/* --------------------------------------------------------------------------------------
Types
-------------------------------------------------------------------------------------- */
type StaggerChildrenProps = HTMLMotionProps<"div">;

/* --------------------------------------------------------------------------------------
Animation
-------------------------------------------------------------------------------------- */
const STAGGER_CHILDREN = {
  animate: {
    transition: {
      staggerChildren: 0.05,
    },
  },
};

/* --------------------------------------------------------------------------------------
Component
-------------------------------------------------------------------------------------- */
const StaggerChildren = (props: StaggerChildrenProps) => {
  return <motion.div {...props} variants={STAGGER_CHILDREN} />;
};

/* --------------------------------------------------------------------------------------
Exports
-------------------------------------------------------------------------------------- */
export { StaggerChildren };
export type { StaggerChildrenProps };
